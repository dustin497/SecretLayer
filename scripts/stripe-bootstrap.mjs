#!/usr/bin/env node
/**
 * One-command Stripe setup for SecretLayer (same Stripe account as mirrorpathai.com).
 *
 * Usage:
 *   STRIPE_SECRET_KEY=sk_test_... pnpm stripe:setup
 *   STRIPE_SECRET_KEY=sk_live_... pnpm stripe:setup -- --live
 */
import Stripe from "stripe";
import { writeFileSync } from "node:fs";
import { resolve } from "node:path";

function resolveStripeSecretKey() {
  return (
    process.env.STRIPE_SECRET_KEY ||
    process.env.stripe ||
    process.env.STRIPE ||
    process.env.BRAND_AGENT_STRIPE_KEY ||
    process.env.BRAND_AGENTS_STRIPE_KEY ||
    process.env.BRAND_AGENT_STRIPE_SECRET_KEY
  );
}

const key = resolveStripeSecretKey();
const live = process.argv.includes("--live");
const webOrigin = process.env.WEB_ORIGIN ?? "https://secretlayer.net";
const apiOrigin = process.env.API_ORIGIN ?? "https://api.secretlayer.net";

if (!key) {
  console.error(`
SecretLayer Stripe bootstrap — missing Stripe secret key

Accepted secret names in Cursor (pick ONE):
  stripe          ← common Cursor name
  STRIPE_SECRET_KEY
  BRAND_AGENT_STRIPE_KEY
  BRAND_AGENTS_STRIPE_KEY

Your Mirror Path AI site uses Payment Link:
  https://buy.stripe.com/bJebJ1dTlfnvdCn4aZbV600

Use the SAME Stripe account (dashboard.stripe.com):

  1. Bank / payouts (if not done): https://dashboard.stripe.com/settings/payouts
  2. API keys: https://dashboard.stripe.com/apikeys
  3. Re-run: STRIPE_SECRET_KEY=sk_test_... pnpm stripe:setup

Or create Payment Links manually (no script):
  https://dashboard.stripe.com/payment-links
`);
  process.exit(1);
}

const stripe = new Stripe(key);

const catalog = [
  { plan: "personal", name: "SecretLayer Personal", amount: 499, description: "Encrypted sync for everyday project work" },
  { plan: "pro", name: "SecretLayer Pro", amount: 999, description: "Credential operations for developers and teams" },
];

const out = {
  STRIPE_SECRET_KEY: key.startsWith("sk_") ? `${key.slice(0, 12)}...` : "(set)",
  WEB_ORIGIN: webOrigin,
  API_ORIGIN: apiOrigin,
};

console.log("SecretLayer Stripe bootstrap\n");
console.log(`Mode: ${live ? "LIVE" : key.includes("_live_") ? "LIVE key" : "test"}\n`);

for (const item of catalog) {
  const product = await stripe.products.create({
    name: item.name,
    description: item.description,
    metadata: { secretlayer_plan: item.plan },
  });

  const price = await stripe.prices.create({
    product: product.id,
    unit_amount: item.amount,
    currency: "usd",
    recurring: { interval: "month" },
    metadata: { secretlayer_plan: item.plan },
  });

  const link = await stripe.paymentLinks.create({
    line_items: [{ price: price.id, quantity: 1 }],
    metadata: { secretlayer_plan: item.plan },
    after_completion: {
      type: "redirect",
      redirect: { url: `${webOrigin}/app?billing=success` },
    },
  });

  const priceEnv = item.plan === "personal" ? "STRIPE_PRICE_PERSONAL" : "STRIPE_PRICE_PRO";
  const linkEnv = item.plan === "personal" ? "STRIPE_PAYMENT_LINK_PERSONAL" : "STRIPE_PAYMENT_LINK_PRO";
  out[priceEnv] = price.id;
  out[linkEnv] = link.url;

  console.log(`✓ ${item.name}`);
  console.log(`  ${priceEnv}=${price.id}`);
  console.log(`  ${linkEnv}=${link.url}\n`);
}

const webhookUrl = `${apiOrigin}/billing/webhook`;
let webhookSecret = process.env.STRIPE_WEBHOOK_SECRET ?? "";

try {
  const endpoint = await stripe.webhookEndpoints.create({
    url: webhookUrl,
    enabled_events: [
      "checkout.session.completed",
      "customer.subscription.updated",
      "customer.subscription.deleted",
    ],
    description: "SecretLayer billing",
  });
  webhookSecret = endpoint.secret ?? webhookSecret;
  console.log(`✓ Webhook endpoint: ${webhookUrl}`);
  if (endpoint.secret) console.log(`  STRIPE_WEBHOOK_SECRET=${endpoint.secret}\n`);
} catch (err) {
  console.log(`! Webhook not created (${err.message}). Add manually: ${webhookUrl}\n`);
}

const envLines = [
  "# Generated by pnpm stripe:setup",
  `STRIPE_SECRET_KEY=${key}`,
  `STRIPE_PRICE_PERSONAL=${out.STRIPE_PRICE_PERSONAL}`,
  `STRIPE_PRICE_PRO=${out.STRIPE_PRICE_PRO}`,
  `STRIPE_PAYMENT_LINK_PERSONAL=${out.STRIPE_PAYMENT_LINK_PERSONAL}`,
  `STRIPE_PAYMENT_LINK_PRO=${out.STRIPE_PAYMENT_LINK_PRO}`,
  webhookSecret ? `STRIPE_WEBHOOK_SECRET=${webhookSecret}` : "# STRIPE_WEBHOOK_SECRET=whsec_...",
  `WEB_ORIGIN=${webOrigin}`,
].join("\n");

const envPath = resolve(process.cwd(), ".env.stripe.generated");
writeFileSync(envPath, envLines + "\n");
console.log(`Wrote ${envPath}`);
console.log("\nNext: copy these values to Railway (api.secretlayer.net) and Netlify env.");
console.log("Bank payouts: https://dashboard.stripe.com/settings/payouts");
